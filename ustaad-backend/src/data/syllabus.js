export function findSubjectByChapterId(chapterId) {
  return subjects.find((subject) =>
    subject.chapters.some((chapter) => chapter.id === chapterId)
  );
}

export function findChapterById(chapterId) {
  for (const subject of subjects) {
    const chapter = subject.chapters.find((item) => item.id === chapterId);
    if (chapter) return chapter;
  }
  return null;
}

/** Map profile grade ("11", "12", "Class 11") to syllabus label */
export function normalizeStudentGrade(userGrade) {
  const value = String(userGrade || "").trim().toLowerCase();
  if (!value) return "Class 11";
  if (value === "12" || value.includes("12")) return "Class 12";
  if (value === "11" || value.includes("11")) return "Class 11";
  return "Class 11";
}

export function getSubjectsForStudentGrade(userGrade) {
  const gradeLabel = normalizeStudentGrade(userGrade);
  return subjects.filter((subject) => subject.grade === gradeLabel);
}

export function getSubjectForStudent(userGrade, subjectId) {
  return getSubjectsForStudentGrade(userGrade).find((subject) => subject.id === subjectId);
}

export function isChapterAllowedForStudent(userGrade, chapterId) {
  return getSubjectsForStudentGrade(userGrade).some((subject) =>
    subject.chapters.some((chapter) => chapter.id === chapterId)
  );
}
